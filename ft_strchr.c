#include "libft.h"

char	*strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (c);
		i++;
	}
	if (s[i] == '\0' && c == '\0')
		return (c);
	return (NULL);
}
