#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	long res;

	i = 0;
	res = 0;
	sign = 1;
	while (str[i] <= 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
			i++;
	}
	return ((int)(sign * res));
}
